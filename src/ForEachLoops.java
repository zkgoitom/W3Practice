// for each loops also known as enhanced for loops are used exclusively to loop through elements in an array
public class ForEachLoops {
    public static void main (String [] args) {
        String [] players = {"Lebron", "Jordan", "Kobe", "Kareem"};
        for (String i : players) {
            System.out.println(i); // loops through each element
        }
    }
}
