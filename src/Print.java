
// the print method is for output
public class Print {
    public static void main (String [] args) {
        // println is a method that output values or print text in java
        // it will add a new line for each method
        System.out.println("Hello world");

        // the print method is similar to println() except that it does not insert a new line at the end of the output
        System.out.print("Hello World");

    }
}
